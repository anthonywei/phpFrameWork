  `Fuin` int(10) unsigned NOT NULL default '0' COMMENT '商家的qq号码',
  `Fwxid` varchar(64) NOT NULL default '' COMMENT '商家的微信号码',
  `Femail` varchar(64) NOT NULL default '' COMMENT '商家的服务邮箱',
  `Fwbtype` int(10) unsigned NOT NULL default '0' COMMENT '商家的微博类型，新浪微博或者腾讯微博',
  `Fwbid` varchar(64) NOT NULL default '' COMMENT '商家的微博帐号',
  `Fmobile0` varchar(16) NOT NULL default '' COMMENT '商家的手机号码',
  `Fmobile1` varchar(16) NOT NULL default '' COMMENT '商家的手机号码',
  `Fphone0` varchar(16) NOT NULL default '' COMMENT '商家的电话号码',
  `Fphone1` varchar(16) NOT NULL default '' COMMENT '商家的电话号码',
  `Fphone2` varchar(16) NOT NULL default '' COMMENT '商家的电话号码',
  `Fproperty` bigint(20) unsigned default '0' COMMENT '商家的用户属性',
