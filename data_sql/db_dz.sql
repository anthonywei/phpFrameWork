  `Fcity_cn` varchar(10) default '' COMMENT '城市的中文名，比如深圳，广州',
  `Fcity_py` varchar(10) default '' COMMENT '城市的中文拼音，比如shenzhen，guangzhou',
  `Fcity_py_sub` varchar(10) default '' COMMENT '城市的中文拼音简写，比如sz，gz',
  `Flongitude` int(10) unsigned default '0' COMMENT '所在位置的经度',
  `Flatitude` int(10) unsigned default '0' COMMENT '所在位置的纬度',
  `Fposition` varchar(1024) default '' COMMENT '商家所在的地址位置文字描述',
  `Fcbd` varchar(64) default '' COMMENT '商家所在的商圈位置文字描述',
  `Fimg0` varchar(64) default '' COMMENT '商家店铺图片，默认这张图为主图',
  `Fimg1` varchar(64) default '' COMMENT '商家店铺图片',
  `Fimg2` varchar(64) default '' COMMENT '商家店铺图片',
  `Fimg3` varchar(64) default '' COMMENT '商家店铺图片',
  `Fimg4` varchar(64) default '' COMMENT '商家店铺图片',
