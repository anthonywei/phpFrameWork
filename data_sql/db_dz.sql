  `Fscore` int(10) unsigned default '0' COMMENT '总体评价得分',
  `Fscore_tasts` int(10) unsigned default '0' COMMENT '味道评价得分',
  `Fscore_env` int(10) unsigned default '0' COMMENT '环境评价得分',
  `Fscore_service` int(10) unsigned default '0' COMMENT '服务评价得分',
  `Findex_score` int(10) unsigned default '0' COMMENT '商家的排序因子，根据排序因子搜索排序',
  `Fstatus` int(10) unsigned default '0' COMMENT '商家的状态',
  `Fadd_time` int(10) unsigned default '0' COMMENT '商家的添加时间',
  `Fmodify_time` int(10) unsigned default '0' COMMENT '商家的修改时间',
  UNIQUE KEY `Fmid` (`Fmid`),
  UNIQUE KEY `Fm_name` (`Fm_name`)
